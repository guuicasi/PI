    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        Product product = new Product();
        if (RegisterPage.isNotEmpty(nameRegisterField.getText()) && RegisterPage.isNotEmpty(priceRegisterField.getText()) && RegisterPage.isNotEmpty(nameRegisterField.getText())) {
            if (Integer.parseInt(registerQtdSpinner.getValue().toString()) > 0) {
                product.setName(nameRegisterField.getText());
                product.setPrice(Double.parseDouble(priceRegisterField.getText()));
                product.setQuantity(Integer.parseInt(registerQtdSpinner.getValue().toString()));

                productService.insert(product);

                Perfumaria.productList = productService.getAllProducts();

                setTable(Perfumaria.productList);

                nameRegisterField.setText("");
                registerQtdSpinner.setValue(0);
                priceRegisterField.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "A quantidade precisa ser maior que 0!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Todos os campos precisam ser preenchidos!");
        } 
    }//GEN-LAST:event_registerButtonActionPerformed

    private void exitButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int options = JOptionPane.showConfirmDialog(null,"Deseja voltar para tela de login?", "Sair",JOptionPane.OK_CANCEL_OPTION);
        if (options == 0) {
            Perfumaria.sallerPage.setVisible(false);
            Perfumaria.loginPage.setVisible(true);
        }
    }

    private void removeProductButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (productsTable.getSelectedRow() != -1) {
            int index = getIndexProductById(idProductSelected);
            if (index >= 0) {
                productService.delete(idProductSelected);

                Perfumaria.productList = productService.getAllProducts();

                setTable(Perfumaria.productList);

                nameEditField.setText("");
                qtdEditField.setValue(0);
                priceEditField.setText("");

                nameEditField.setEnabled(false);
                qtdEditField.setEnabled(false);
                priceEditField.setEnabled(false);
                editButton.setEnabled(false);
                removeProductButton.setEnabled(false);
            }
        }
    }

    private void setTable(List<Product> products) {
        productsTable.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {
                "Id", "Nome do Produto", "Qtd", "Pre√ßo"
            }
            
        ) {
            boolean[] canEdit = new boolean [] {
                false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });

        DefaultTableModel dtmProducts = (DefaultTableModel) productsTable.getModel();
        
        for (Product product : products) {
            Object[] data = {product.id, product.name, product.quantity, product.price.toString()};
            dtmProducts.addRow(data);
        }
    }

    public void tableMouseClicked(java.awt.event.MouseEvent evt) {
        if(productsTable.getSelectedRow() != -1){
            idProductSelected = Integer.parseInt(productsTable.getValueAt(productsTable.getSelectedRow(), 0).toString());
            int indexSelected =  getIndexProductById(idProductSelected);

            nameEditField.setText(productsTable.getValueAt(productsTable.getSelectedRow(), 1).toString());
            qtdEditField.setValue(Integer.parseInt(productsTable.getValueAt(productsTable.getSelectedRow(), 2).toString()));
            priceEditField.setText(productsTable.getValueAt(productsTable.getSelectedRow(), 3).toString());

            nameEditField.setEnabled(true);
            qtdEditField.setEnabled(true);
            priceEditField.setEnabled(true);
            editButton.setEnabled(true);
            removeProductButton.setEnabled(true);
        }
    }

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if(productsTable.getSelectedRow() != -1){
            int index = getIndexProductById(idProductSelected);
            if (index >= 0) {
                Product product = Perfumaria.productList.get(index);
                product.setName(nameEditField.getText()); 
                product.setPrice(Double.parseDouble(priceEditField.getText()));
                product.setQuantity(Integer.parseInt(qtdEditField.getValue().toString()));

                productsTable.setValueAt(nameEditField.getText(), productsTable.getSelectedRow(), 1);
                productsTable.setValueAt(qtdEditField.getValue().toString(), productsTable.getSelectedRow(), 2);
                productsTable.setValueAt(priceEditField.getText(), productsTable.getSelectedRow(), 3);

                productService.update(product);

                Perfumaria.productList = productService.getAllProducts();

                nameEditField.setText("");
                qtdEditField.setValue(0);
                priceEditField.setText("");

                nameEditField.setEnabled(false);
                qtdEditField.setEnabled(false);
                priceEditField.setEnabled(false);  
                editButton.setEnabled(false);
                removeProductButton.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Algo inesperado aconteceu, selecione novamente um produto.");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto!");
        }
    }//GEN-LAST:event_editButtonActionPerformed


    private int getIndexProductById(int id) {
        for (Product product : Perfumaria.productList) {
            if (product.id == id) {
                return Perfumaria.productList.indexOf(product);
            };  
        }
        return -1;
    }

